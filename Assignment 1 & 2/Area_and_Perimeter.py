Length = float(input("Enter the length of rectangle: "))

Breadth = float(input("Enter the Breadth of rectangle: "))

Radius = float(input("Enter the radius of circle: "))

Area_of_Rectangle = Length * Breadth

Perimeter_of_Rectangle = 2 * (Length + Breadth)

Area_of_Circle = 3.14 * Radius * Radius

Circumference_of_Circle = 2 * 3.14 * Radius

print(f"Area of Rectangle: {Area_of_Rectangle}")

print(f"Perimeter of Rectangle: {Perimeter_of_Rectangle}")

print(f"Area of Circle: {Area_of_Circle}")

print(f"Circumference of Circle: {Circumference_of_Circle}")